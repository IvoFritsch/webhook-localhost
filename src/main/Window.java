    public final void updateList(){
        DefaultListModel<String> model = (DefaultListModel)jList1.getModel();
        model.removeAllElements();
        Main.getHooks().forEach(h -> {
            model.addElement(h.getAlias());
        });
    }
    
    public final void clearHookDetailsFields(){
        endpointField.setText("");
        forwardToField.setText("");
        aliasField.setText("");
    }

    public final void updateHookDetailsFields(){
        Webhook hook = Main.getHook(selectedHookIndex);
        endpointField.setText(hook.getEndpoint());
        forwardToField.setText(hook.getDispatchTo());
        aliasField.setText(hook.getAliasLiteral());
    }
    
