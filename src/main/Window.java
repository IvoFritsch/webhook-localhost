    private void saveChangesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangesBtnActionPerformed
        Webhook update = Main.getHook(selectedHookIndex);
        update.setAlias(aliasField.getText());
        update.setDispatchTo(forwardToField.getText());
        updateList();
        clearHookDetailsFields();
    }//GEN-LAST:event_saveChangesBtnActionPerformed

    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        if(!evt.getValueIsAdjusting()){
            selectedHookIndex = jList1.getSelectedIndex();
            updateHookDetailsFields();
        }
    }//GEN-LAST:event_jList1ValueChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Webhook webhook = new Webhook();
        if(!webhook.isReady()) return;
        if(!webhook.connect()) return;
        Main.getHooks().add(webhook);
        updateList();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Webhook hook = Main.getHook(selectedHookIndex);
        Toolkit.getDefaultToolkit().getSystemClipboard().setContents(new StringSelection(hook.getEndpoint()), null);
    }//GEN-LAST:event_jButton4ActionPerformed


